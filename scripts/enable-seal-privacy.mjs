#!/usr/bin/env node
/**
 * Generate or reuse NAMI_SEAL_EVIDENCE_KEY and print Render enablement steps.
 *
 * Usage:
 *   node scripts/enable-seal-privacy.mjs
 *   node scripts/enable-seal-privacy.mjs --probe-only --probe https://nami-backend-rv0o.onrender.com
 *   node scripts/enable-seal-privacy.mjs --key <64-char-hex>
 *   node scripts/enable-seal-privacy.mjs --regenerate
 */
import crypto from 'node:crypto';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');
const keyFilePath = path.join(rootDir, 'deployments', 'testnet', 'seal-evidence-key.txt');
const renderEnvPath = path.join(rootDir, 'deployments', 'testnet', 'render.env');

function readArg(flag) {
  const index = process.argv.indexOf(flag);
  return index >= 0 ? process.argv[index + 1] ?? '' : '';
}

function hasFlag(flag) {
  return process.argv.includes(flag);
}

function generateKey() {
  return crypto.randomBytes(32).toString('hex');
}

function readExistingKey() {
  if (!fs.existsSync(keyFilePath)) {
    return '';
  }

  for (const line of fs.readFileSync(keyFilePath, 'utf8').split('\n')) {
    if (!line.startsWith('NAMI_SEAL_EVIDENCE_KEY=')) {
      continue;
    }

    const value = line.slice('NAMI_SEAL_EVIDENCE_KEY='.length).trim();

    if (/^[0-9a-fA-F]{64}$/.test(value)) {
      return value;
    }
  }

  return '';
}

function upsertRenderEnv(key) {
  const lines = fs.existsSync(renderEnvPath)
    ? fs.readFileSync(renderEnvPath, 'utf8').split('\n')
    : [];

  const next = [];
  let wroteEnabled = false;
  let wroteKey = false;

  for (const line of lines) {
    if (line.startsWith('NAMI_SEAL_PRIVACY_ENABLED=')) {
      next.push('NAMI_SEAL_PRIVACY_ENABLED=true');
      wroteEnabled = true;
      continue;
    }

    if (line.startsWith('NAMI_SEAL_EVIDENCE_KEY=')) {
      next.push(`NAMI_SEAL_EVIDENCE_KEY=${key}`);
      wroteKey = true;
      continue;
    }

    next.push(line);
  }

  if (!wroteEnabled || !wroteKey) {
    if (next.length > 0 && next[next.length - 1] !== '') {
      next.push('');
    }

    next.push('# Phase 9.2 — Seal privacy envelopes');
    next.push('NAMI_SEAL_PRIVACY_ENABLED=true');
    next.push(`NAMI_SEAL_EVIDENCE_KEY=${key}`);
  }

  fs.mkdirSync(path.dirname(renderEnvPath), { recursive: true });
  fs.writeFileSync(renderEnvPath, next.join('\n').replace(/\n*$/, '\n'));
}

function writeKeyFiles(key) {
  fs.mkdirSync(path.dirname(keyFilePath), { recursive: true });
  fs.writeFileSync(
    keyFilePath,
    [
      '# Generated by scripts/enable-seal-privacy.mjs — keep secret, never commit',
      'NAMI_SEAL_PRIVACY_ENABLED=true',
      `NAMI_SEAL_EVIDENCE_KEY=${key}`,
      '',
    ].join('\n')
  );

  upsertRenderEnv(key);

  const localBackendEnv = path.join(rootDir, 'backend', '.env');

  if (!fs.existsSync(localBackendEnv)) {
    return;
  }

  const localLines = fs.readFileSync(localBackendEnv, 'utf8').split('\n');
  const localNext = [];
  let wroteEnabled = false;
  let wroteKey = false;

  for (const line of localLines) {
    if (line.startsWith('NAMI_SEAL_PRIVACY_ENABLED=')) {
      localNext.push('NAMI_SEAL_PRIVACY_ENABLED=true');
      wroteEnabled = true;
      continue;
    }

    if (line.startsWith('NAMI_SEAL_EVIDENCE_KEY=')) {
      localNext.push(`NAMI_SEAL_EVIDENCE_KEY=${key}`);
      wroteKey = true;
      continue;
    }

    if (line.startsWith('# NAMI_SEAL_EVIDENCE_KEY=')) {
      localNext.push(`NAMI_SEAL_EVIDENCE_KEY=${key}`);
      wroteKey = true;
      continue;
    }

    localNext.push(line);
  }

  if (!wroteEnabled) {
    localNext.push('');
    localNext.push('NAMI_SEAL_PRIVACY_ENABLED=true');
  }

  if (!wroteKey) {
    localNext.push(`NAMI_SEAL_EVIDENCE_KEY=${key}`);
  }

  fs.writeFileSync(localBackendEnv, localNext.join('\n').replace(/\n*$/, '\n'));
}

async function probeIndexer(baseUrl) {
  const normalized = baseUrl.replace(/\/$/, '');
  const response = await fetch(`${normalized}/api/ops/launch-summary`);

  if (!response.ok) {
    throw new Error(`launch-summary failed (${response.status})`);
  }

  const summary = await response.json();
  const seal = summary.seal_privacy ?? {};

  return {
    enabled: Boolean(seal.enabled),
    keyConfigured: Boolean(seal.key_configured),
    sealedCount: Number(seal.sealed_count ?? 0),
  };
}

const providedKey = readArg('--key').trim();
const probeUrl = readArg('--probe').trim();
const probeOnly = hasFlag('--probe-only');
const regenerate = hasFlag('--regenerate');

let key = '';

if (/^[0-9a-fA-F]{64}$/.test(providedKey)) {
  key = providedKey;
} else if (!regenerate) {
  key = readExistingKey();
}

if (!key) {
  key = generateKey();
}

const createdNewKey = !readExistingKey() || regenerate || Boolean(providedKey);

if (!probeOnly) {
  writeKeyFiles(key);
}

console.log('');
console.log('Seal privacy enablement (Phase 9.2)');
console.log('====================================');
console.log('');

if (probeOnly) {
  console.log('Probe-only mode — local key files were not changed.');
} else {
  console.log(createdNewKey ? 'Key status: new or updated key written.' : 'Key status: reused existing key.');
  console.log('Key file:    ', path.relative(rootDir, keyFilePath));
  console.log('Render env:  ', path.relative(rootDir, renderEnvPath));
  if (fs.existsSync(path.join(rootDir, 'backend', '.env'))) {
    console.log('Local env:   ', path.relative(rootDir, 'backend/.env'));
  }
}

console.log('');
console.log('Render dashboard (nami-backend → Environment):');
console.log('  NAMI_SEAL_PRIVACY_ENABLED=true');
console.log(`  NAMI_SEAL_EVIDENCE_KEY=${key}`);
console.log('');
console.log('After saving env vars, click Manual Deploy on Render, then verify:');
console.log('  node scripts/enable-seal-privacy.mjs --probe-only --probe https://nami-backend-rv0o.onrender.com');
console.log('');

if (probeUrl) {
  try {
    const readiness = await probeIndexer(probeUrl);
    console.log('Probe:', probeUrl);
    console.log('  enabled:         ', readiness.enabled ? 'yes' : 'no');
    console.log('  key configured:  ', readiness.keyConfigured ? 'yes' : 'no');
    console.log('  sealed packets:  ', readiness.sealedCount);
    console.log('');

    if (!readiness.keyConfigured) {
      console.log('Render still needs NAMI_SEAL_EVIDENCE_KEY saved and a redeploy.');
      process.exitCode = 1;
    }
  } catch (error) {
    console.error(error instanceof Error ? error.message : error);
    process.exitCode = 1;
  }
}